package org.xinyo.domain;

public class FullPoetry extends Poetry {
    private String authorSp;
    private String titleSp;
    private String paragraphsSp;

    public String getAuthorSp() {
        return authorSp;
    }

    public void setAuthorSp(String authorSp) {
        this.authorSp = authorSp;
    }

    public String getTitleSp() {
        return titleSp;
    }

    public void setTitleSp(String titleSp) {
        this.titleSp = titleSp;
    }

    public String getParagraphsSp() {
        return paragraphsSp;
    }

    public void setParagraphsSp(String paragraphsSp) {
        this.paragraphsSp = paragraphsSp;
    }
}
